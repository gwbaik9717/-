T = int(input())
# 여러개의 테스트 케이스가 주어지므로, 각각을 처리합니다.
for i in range(1, T + 1):
    # ///////////////////////////////////////////////////////////////////////////////////
    P, Q, R, S, W = map(int, input().split())
    costA = W * P
    costB = Q + max(0, W - R) * S

    print(f'#{i} {min(costA, costB)}')

    # ///////////////////////////////////////////////////////////////////////////////////